#ifndef FUNCFORCLIENT_H
#define FUNCFORCLIENT_H

// Подключение заголовочного файла с реализацией клиента-синглтона
#include "singleton_client.h"

#include <QObject>  // Базовый класс Qt для работы с сигналами/слотами

// Функция проверки авторизации пользователя
// Параметры:
//   log - логин пользователя
//   pass - пароль пользователя
// Возвращает:
//   true - если авторизация успешна, false - в противном случае
bool is_auth(QString log, QString pass);

// Функция регистрации нового пользователя
// Параметры:
//   email - электронная почта пользователя
//   log - логин пользователя
//   pass - пароль пользователя
// Возвращает:
//   true - если регистрация успешна, false - в противном случае
bool is_reg(QString email, QString log, QString pass);

// Функция получения результатов графика минимизации
// Возвращает:
//   true - если операция выполнена успешно, false - в противном случае
bool result_graf_min();

// Функция реализации метода деления пополам (halving method)
// Параметры:
//   [QString] - параметры метода (уточнить назначение каждого)
// Возвращает:
//   true - если метод выполнен успешно, false - в противном случае
bool result_halving_method(QString, QString, QString, QString);

#endif // FUNCFORCLIENT_H
